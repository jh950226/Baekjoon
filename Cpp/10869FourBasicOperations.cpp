#include <iostream>

int main() {
    int A, B;
    std::cin >> A >> B;           // A와 B 입력 받기
    std::cout << A + B << std::endl;  // A + B 출력
    std::cout << A - B << std::endl;
    std::cout << A * B << std::endl;
    std::cout << A / B << std::endl;
    std::cout << A % B << std::endl;
    return 0;
}
